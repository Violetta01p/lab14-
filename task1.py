# Створення або додавання запису до щоденника

# Користувач вводить дату, локацію та текст нотатки
date = input("Введіть дату (наприклад, 2023-04-05): ")
location = input("Введіть локацію: ")
text = input("Введіть текст нотатки: ")

# Відкриваємо файл у режимі додавання (створюється, якщо не існує)
with open("diary.txt", "a", encoding="utf-8") as file:
    # Записуємо інформацію в структурованому вигляді
    file.write(f"Дата: {date}\n")
    file.write(f"Локація: {location}\n")
    file.write(f"Текст: {text}\n")
    file.write("-" * 40 + "\n")  # розділювач між записами

print("Запис додано до щоденника!")
